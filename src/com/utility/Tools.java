package com.utility;

public class Tools {
    static public final int regSize = 8;

    public static void printTab(int tab) {
        for (int i = 0; i < tab; ++i) {
            System.out.print('\t');
        }
    }

    public static void haha() {
        puts("---haha---");
    }

    public static void puts(String s) {
        System.out.println(s);
    }
}
